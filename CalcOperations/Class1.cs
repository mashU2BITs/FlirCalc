namespace CalcOperations
{
    public class CalcOps
    {
        public  float addition(float valA, float valB)
        {
            return valA + valB;
        }

        public  float subtraction(float valA, float valB)
        {
            return valA - valB;
        }

        public  float multiplication(float valA, float valB)
        {
            return valA * valB;
        }

        public  float division(float valA, float valB)
        {
            return valA / valB;
        }
    }
}